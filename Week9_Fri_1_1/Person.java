package Week9_Fri_1_1;

public class Person {
	protected String name = "Orisa";
	protected int age = 15;
	protected double height = 200.0;
	protected double weight = 1000.0;

	public String getName() {
		return this.name;
	}

	public int getAge() {
		return this.age;
	}

	public double getHeight() {
		return this.height;
	}

	public double getWeight() {
		return this.weight;
	}
}